/** Look up a filmography by condition ID */
export function getFilmography(conditionId: string): PersonFilmography | undefined {
  return PERSON_FILMOGRAPHIES.find(p => p.conditionId === conditionId);
}

/** Get just the TMDB IDs for a condition — used by the API route */
export function getFilmographyIds(conditionId: string): number[] {
  return getFilmography(conditionId)?.films.map(f => f.tmdbId) ?? [];
}

/** Map of conditionId → PersonFilmography for O(1) lookup */
export const FILMOGRAPHY_MAP = new Map<string, PersonFilmography>(
  PERSON_FILMOGRAPHIES.map(p => [p.conditionId, p])
);
