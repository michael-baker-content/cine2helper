export function getSortedOscarWinners(): OscarWinner[] {
  const seen = new Set<number>();
  return OSCAR_WINNERS
    .filter((f) => {
      if (seen.has(f.tmdbId)) return false;
      seen.add(f.tmdbId);
      return true;
    })
    .sort((a, b) => b.year - a.year);
}

