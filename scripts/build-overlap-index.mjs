/**
 * build-overlap-index.mjs
 *
 * Computes which win conditions each film qualifies for and writes the
 * result to lib/overlap-index.ts — a Record<number, string[]> mapping
 * TMDB film ID to an array of condition IDs.
 *
 * Run after build-movie-cache.mjs whenever win conditions or film lists change:
 *   npx tsx scripts/build-overlap-index.mjs
 *
 * No TMDB calls — purely a static data computation.
 */

import { writeFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');

// ── Load all source data ──────────────────────────────────────────────────────

console.log('Loading source data...');

const [
  { WIN_CONDITIONS },
  { OSCAR_WINNER_TMDB_IDS },
  { SERVICE_THE_FANS_IDS },
  { CURATED_LISTS },
  { PERSON_FILMOGRAPHIES },
] = await Promise.all([
  import('../lib/win-conditions.ts'),
  import('../lib/oscar-winners.ts'),
  import('../lib/service-the-fans.ts'),
  import('../lib/decade-genre-lists.ts'),
  import('../lib/person-filmographies.ts'),
]);

// ── Build a film set per condition ────────────────────────────────────────────
// This mirrors the logic in app/api/movies/route.ts so the two stay in sync.
// If you add a new condition type to the route, add it here too.

function getFilmIdsForCondition(condition) {
  const { id, category } = condition;

  if (id === 'thank-the-academy') {
    return new Set(OSCAR_WINNER_TMDB_IDS);
  }

  if (id === 'service-the-fans') {
    return new Set(SERVICE_THE_FANS_IDS);
  }

  if (category === 'person' || category === 'themed') {
    // Group condition — merge all people's filmographies
    if (condition.groupPersonIds?.length > 0) {
      const ids = new Set();
      for (const personId of condition.groupPersonIds) {
        const filmography = PERSON_FILMOGRAPHIES.find(f => f.personId === personId);
        if (filmography) {
          for (const film of filmography.films) ids.add(film.tmdbId);
        }
      }
      return ids;
    }
    // Individual person condition
    const filmography = PERSON_FILMOGRAPHIES.find(f => f.conditionId === id);
    if (filmography) {
      return new Set(filmography.films.map(f => f.tmdbId));
    }
    return new Set();
  }

  if (category === 'decade') {
    const curated = CURATED_LISTS[id];
    if (curated?.length > 0) {
      return new Set(curated.map(f => f.tmdbId));
    }
    return new Set();
  }

  // genre / country / keyword — these are live TMDB discover queries,
  // can't be precomputed statically. Skip for now.
  return new Set();
}

// ── Compute overlap index ─────────────────────────────────────────────────────

console.log(`Computing overlaps for ${WIN_CONDITIONS.length} win conditions...`);

// Build film sets for every condition upfront
const conditionFilmSets = new Map();
for (const condition of WIN_CONDITIONS) {
  const ids = getFilmIdsForCondition(condition);
  if (ids.size > 0) {
    conditionFilmSets.set(condition.id, ids);
    console.log(`  ${condition.id}: ${ids.size} films`);
  } else {
    console.log(`  ${condition.id}: skipped (live query or no data)`);
  }
}

// Collect all unique film IDs across all conditions
const allFilmIds = new Set();
for (const ids of conditionFilmSets.values()) {
  for (const id of ids) allFilmIds.add(id);
}

console.log(`\nTotal unique film IDs: ${allFilmIds.size}`);
console.log('Building overlap index...');

// For each film, find all conditions it qualifies for
const overlapIndex = new Map(); // filmId -> string[]

for (const filmId of allFilmIds) {
  const qualifyingConditions = [];
  for (const [conditionId, filmSet] of conditionFilmSets) {
    if (filmSet.has(filmId)) {
      qualifyingConditions.push(conditionId);
    }
  }
  // Only store films that appear in 2+ conditions (single-condition films
  // have no useful overlap data)
  if (qualifyingConditions.length >= 2) {
    overlapIndex.set(filmId, qualifyingConditions);
  }
}

console.log(`Films with 2+ condition overlaps: ${overlapIndex.size}`);

// ── Write lib/overlap-index.ts ────────────────────────────────────────────────

const timestamp = new Date().toISOString();
const entries = [...overlapIndex.entries()]
  .sort(([a], [b]) => a - b) // sort by film ID for stable diffs
  .map(([id, conditions]) => `  ${id}: [${conditions.map(c => JSON.stringify(c)).join(', ')}]`)
  .join(',\n');

const content = `/**
 * lib/overlap-index.ts — AUTO-GENERATED. DO NOT EDIT MANUALLY.
 *
 * Generated by scripts/build-overlap-index.mjs
 * Last updated: ${timestamp}
 *
 * Maps TMDB film ID → array of win condition IDs the film qualifies for.
 * Only includes films that appear in 2 or more conditions.
 *
 * To regenerate:
 *   npx tsx scripts/build-overlap-index.mjs
 */

export const OVERLAP_INDEX: Record<number, string[]> = {
${entries}
};
`;

const outputPath = resolve(projectRoot, 'lib', 'overlap-index.ts');
writeFileSync(outputPath, content, 'utf-8');

console.log(`\n✓ Written to lib/overlap-index.ts (${overlapIndex.size} entries, ${(content.length / 1024).toFixed(0)} KB)`);
console.log('Done! Commit lib/overlap-index.ts and redeploy.\n');
